

function changePage(page) {
    switch(page){
        case 'home' :
        window.location = "index.html/home.html";
        window.location.href = "https://www.google.com";
        break;
        case 'page_1' :
            window.location = "https://www.google.com";
            window.location.href = "https://www.google.com";
            break;
        case 'page_2' :
        window.location = "https://www.google.com";
        window.location.href = "https://www.google.com";
        break;
    }

}